public class Car {
  private   String name;
  private String carBrend;
   private String color;

   void setName(String name){
       this.name=name;
   }
   void setCarBrend(String carBrend){
       this.carBrend=carBrend;
   }
   void setColor(String color){
       this.color=color;
   }
   String getName(){
       return name;
   }
   String getCarBrend(){
       return carBrend;
   }
   String getColor(){
       return color;
   }
}
